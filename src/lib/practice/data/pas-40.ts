import { Exercise, PatternNote, Hand, NoteType } from '../types';

// Helper to build normal notes
function n(position: number, hand: Hand, type: NoteType = 'normal'): PatternNote {
  return { position, hand, type };
}

// Helper for flam notes (grace note before main)
function flam(position: number, mainHand: Hand): PatternNote {
  const graceHand: Hand = mainHand === 'R' ? 'L' : 'R';
  return {
    position, hand: mainHand, type: 'flam',
    graceNotes: [{ hand: graceHand, offsetMs: 20 }],
  };
}

// Helper for drag notes (two grace notes before main)
function drag(position: number, mainHand: Hand): PatternNote {
  const graceHand: Hand = mainHand === 'R' ? 'L' : 'R';
  return {
    position, hand: mainHand, type: 'drag',
    graceNotes: [
      { hand: graceHand, offsetMs: 35 },
      { hand: graceHand, offsetMs: 20 },
    ],
  };
}

